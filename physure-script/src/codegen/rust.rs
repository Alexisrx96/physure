use physure_core::error::PhysureResult;
use crate::ast::{BinaryOp, Expr, Statement, UnaryOp};
use super::CodeGenerator;
use std::collections::HashSet;

pub struct RustCodeGenerator {
    known_vars: HashSet<String>,
    known_fns: HashSet<String>,
}

impl RustCodeGenerator {
    pub fn new() -> Self {
        Self {
            known_vars: HashSet::new(),
            known_fns: HashSet::new(),
        }
    }

    fn clean_unit(u: &str) -> String {
        u.trim()
            .replace(" / ", "/")
            .replace(" * ", "*")
            .replace(" ^ ", "^")
    }
}

impl CodeGenerator for RustCodeGenerator {
    fn generate_program(&mut self, statements: &[Statement]) -> PhysureResult<String> {
        let mut out = String::new();
        out.push_str("// Generated by Physure Native AST Codegen\n");
        out.push_str("use physure::quantity::Quantity;\n\n");
        out.push_str("fn main() -> Result<(), Box<dyn std::error::Error>> {\n");

        for stmt in statements {
            let stmt_code = self.generate_statement(stmt)?;
            if !stmt_code.is_empty() {
                out.push_str(&format!("    {}\n", stmt_code));
            }
        }

        out.push_str("    Ok(())\n");
        out.push_str("}\n");
        Ok(out)
    }

    fn generate_statement(&mut self, stmt: &Statement) -> PhysureResult<String> {
        match stmt {
            Statement::Assign { name, expr } => {
                let expr_str = self.generate_expr(expr)?;
                let decl = if self.known_vars.contains(name) {
                    format!("{} = ", name)
                } else {
                    self.known_vars.insert(name.clone());
                    format!("let mut {} = ", name)
                };
                Ok(format!("{}{};", decl, expr_str))
            }
            Statement::Query { expr } => {
                let expr_str = self.generate_expr(expr)?;
                Ok(format!("println!(\"{{}}\", {});", expr_str))
            }
            Statement::AssignAndQuery { name, expr } => {
                let expr_str = self.generate_expr(expr)?;
                let decl = if self.known_vars.contains(name) {
                    format!("{} = ", name)
                } else {
                    self.known_vars.insert(name.clone());
                    format!("let mut {} = ", name)
                };
                Ok(format!("{}{};\n    println!(\"{{}}\", {});", decl, expr_str, name))
            }
            Statement::Assert { left, right, op: _ } => {
                let l_str = self.generate_expr(left)?;
                let r_str = self.generate_expr(right)?;
                Ok(format!("println!(\"{{}}\", ({}.canonical_magnitude() - {}.canonical_magnitude()).abs() <= 0.5);", l_str, r_str))
            }
            Statement::FnDef { name, params, body } => {
                self.known_fns.insert(name.clone());
                let params_str = params.iter().map(|p| format!("{}: &Quantity", p.name)).collect::<Vec<_>>().join(", ");
                let mut body_str = String::new();
                for b_stmt in body {
                    body_str.push_str(&format!("        {}\n", self.generate_statement(b_stmt)?));
                }
                Ok(format!("let {} = |{}| -> Result<Quantity, Box<dyn std::error::Error>> {{\n{}        Ok(Quantity::new(0.0, \"\")?)\n    }};", name, params_str, body_str))
            }
            Statement::DisplayText(text) => {
                let escaped = text.replace('\\', "\\\\").replace('"', "\\\"").replace('\n', "\\n");
                Ok(format!("println!(\"{}\");", escaped))
            }
            Statement::ExprStmt(expr) => {
                let expr_str = self.generate_expr(expr)?;
                Ok(format!("println!(\"{{}}\", {});", expr_str))
            }
        }
    }

    fn generate_expr(&mut self, expr: &Expr) -> PhysureResult<String> {
        match expr {
            Expr::Number(n) => Ok(format!("Quantity::new({:?}, \"\")?", n)),
            Expr::Ident(name) => Ok(format!("{}.clone()", name)),
            Expr::StringLiteral(s) => Ok(format!("\"{}\"", s)),
            Expr::Unary { op, expr } => {
                let inner = self.generate_expr(expr)?;
                match op {
                    UnaryOp::Neg => Ok(format!("{}.mul(&Quantity::new(-1.0, \"\")?)?", inner)),
                    UnaryOp::Sqrt => Ok(format!("{}.sqrt()?", inner)),
                }
            }
            Expr::Binary { op, left, right } => {
                let l_str = self.generate_expr(left)?;
                let r_str = self.generate_expr(right)?;
                match op {
                    BinaryOp::Add => Ok(format!("{}.add(&{})?", l_str, r_str)),
                    BinaryOp::Sub => Ok(format!("{}.sub(&{})?", l_str, r_str)),
                    BinaryOp::Mul => Ok(format!("{}.mul(&{})?", l_str, r_str)),
                    BinaryOp::Div => Ok(format!("{}.div(&{})?", l_str, r_str)),
                    BinaryOp::Pow => Ok(format!("{}.pow({}.canonical_magnitude())?", l_str, r_str)),
                    BinaryOp::Gt | BinaryOp::Lt | BinaryOp::Eq | BinaryOp::Neq | BinaryOp::Gte | BinaryOp::Lte => {
                        let op_str = match op {
                            BinaryOp::Gt => ">",
                            BinaryOp::Lt => "<",
                            BinaryOp::Eq => "==",
                            BinaryOp::Neq => "!=",
                            BinaryOp::Gte => ">=",
                            BinaryOp::Lte => "<=",
                            _ => "==",
                        };
                        Ok(format!("{{ let res = {}.canonical_magnitude() {} {}.canonical_magnitude(); if res {{ Quantity::new(1.0, \"\")? }} else {{ Quantity::new(0.0, \"\")? }} }}", l_str, op_str, r_str))
                    }
                    BinaryOp::ApproxEq => {
                        Ok(format!("{{ let res = ({}.canonical_magnitude() - {}.canonical_magnitude()).abs() <= 0.5; if res {{ Quantity::new(1.0, \"\")? }} else {{ Quantity::new(0.0, \"\")? }} }}", l_str, r_str))
                    }
                }
            }
            Expr::ImplicitMul { left, right } => {
                if let Expr::Number(n) = **left {
                    if let Expr::Ident(ref u) = **right {
                        let clean_u = Self::clean_unit(u);
                        return Ok(format!("Quantity::new({:?}, \"{}\")?", n, clean_u));
                    }
                }
                let l_str = self.generate_expr(left)?;
                let r_str = self.generate_expr(right)?;
                Ok(format!("{}.mul(&{})?", l_str, r_str))
            }
            Expr::Call { name, args } => {
                let formatted_args = args.iter().map(|a| self.generate_expr(a)).collect::<PhysureResult<Vec<_>>>()?.join(", ");
                if self.known_fns.contains(name) {
                    Ok(format!("{}(&{})?", name, formatted_args))
                } else {
                    Ok(format!("Quantity::new(0.0, \"\")?"))
                }
            }
            Expr::Convert { expr, target_unit } => {
                let inner = self.generate_expr(expr)?;
                let clean_u = Self::clean_unit(target_unit);
                Ok(format!("{}.to(\"{}\")?", inner, clean_u))
            }
            Expr::FormatSig { expr, spec: _ } => self.generate_expr(expr),
            Expr::Uncertainty { val, unc } => {
                let v_str = self.generate_expr(val)?;
                let u_str = self.generate_expr(unc)?;
                Ok(format!("Quantity::new_scalar(({}).canonical_magnitude(), ({}).canonical_magnitude(), ({})?.unit, None, None)", v_str, u_str, v_str))
            }
            Expr::Vector(elems) => {
                let q_elems = elems.iter().map(|e| self.generate_expr(e)).collect::<PhysureResult<Vec<_>>>()?.join(", ");
                Ok(format!("vec![{}]", q_elems))
            }
            _ => Ok(format!("Quantity::new(0.0, \"\")?")),
        }
    }
}
